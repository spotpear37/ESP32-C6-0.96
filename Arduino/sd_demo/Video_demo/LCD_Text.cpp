// 绘制单个字符位图到屏幕 (列反转以修正镜像)
static void drawCharBitmap(int x, int y, const uint8_t* bitmap, uint16_t color) {
  uint16_t line[10];  // 每行10像素
  for (int row = 0; row < 14; row++) {
    for (int col = 0; col < 10; col++) {
      // 列反转：从右往左取像素
      line[col] = bitmap[row * 10 + (9 - col)] ? color : RGB565(0, 0, 0);
    }
    if (y + row < LCD_HEIGHT) {
      LCD_addWindow(x, y + row, x + 9, y + row, line);
    }
  }
}

void LCD_ShowError(const char* msg) {
  // 忽略传入的msg，固定显示 "NO SD CARD"
  (void)msg;
  
  LCD_FillScreen(RGB565(0, 0, 0));
  uint16_t white = RGB565(255, 255, 255);
  
  // "NO SD CARD" 共10个字符，每个10像素宽+2像素间距 = 12像素
  // 总宽 10*12-2 = 118，居中 x = (160-118)/2 = 21
  // 高度14，居中 y = (80-14)/2 = 33
  int startX = 21;
  int startY = 33;
  int charW = 12;  // 10像素字符 + 2像素间距
  
  const uint8_t* chars[] = {char_N, char_O, char_space, char_S, char_D, char_space, char_C, char_A, char_R, char_D};
  
  for (int i = 0; i < 10; i++) {
    drawCharBitmap(startX + i * charW, startY, chars[i], white);
  }
  
}
